import java.util.*;
public class collectionmethod{
    public static void main(String args[]){
        ArrayList<integer> arr=new ArrayList<integer>();
        arr.add(2);
        arr.add(3);
        arr.add(1);
        arr.add(7);
        arr.add(0);
        for(integer a:arr){
            System.out.println(a);
        }
    }
}